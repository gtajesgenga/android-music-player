public class Artist {

    private String name;
    private Integer albums_count;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Integer getAlbums_count() {
        return albums_count;
    }

    public void setAlbums_count(Integer albums_count) {
        this.albums_count = albums_count;
    }
}
